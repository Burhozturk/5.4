public class Main
{
    public static void main(String[] args)
    {
        int miles[]={1,3,9,10};
        double kilometers[]={1.609,3.218,14.481,16.090};
        System.out.println("Miles");
        for(int a=0;a<miles.length;a++)
        {
            System.out.println(miles[a]);
        }
        System.out.println("Kilometers");
        for(int b=0;b<kilometers.length;b++)
        {
            System.out.println(kilometers[b]);
        }

    }
}
